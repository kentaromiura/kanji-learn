import fs from "node:fs";

const [, , inputPath = "/private/tmp/ulrike-joyo-kanji.json"] = process.argv;
const outputPath = new URL("../src/UlrikeMnemonicData.res", import.meta.url);

const raw = JSON.parse(fs.readFileSync(inputPath, "utf8"));
const entries = Object.values(raw.data ?? {})
  .map(entry => {
    const kanji = entry.glyph;
    const mnemonicHtml = entry["0"]?.mnemonics_value;
    if (typeof kanji !== "string" || typeof mnemonicHtml !== "string") {
      return undefined;
    }

    const mnemonic = htmlToText(mnemonicHtml);
    if (mnemonic.length === 0) {
      return undefined;
    }

    return {kanji, mnemonic};
  })
  .filter(Boolean)
  .sort((a, b) => a.kanji.localeCompare(b.kanji, "ja"));

const cases = entries
  .map(({kanji, mnemonic}) => `  | ${JSON.stringify(kanji)} => Some(${JSON.stringify(mnemonic)})`)
  .join("\n");

const source = `// Generated by scripts/generate-ulrike-mnemonic-data.mjs.
// Source: https://japanesestudies.github.io/joyo-kanji/complete-ulrike-joyo-kanji.json

let source = "https://www.joyokanji.com/ulrikes-mnemonics"
let jsonSource = "https://japanesestudies.github.io/joyo-kanji/complete-ulrike-joyo-kanji.json"

let forKanji = kanji =>
  switch kanji {
${cases}
  | _ => None
  }
`;

fs.writeFileSync(outputPath, source);
console.log(`Wrote ${entries.length} Ulrike mnemonic entries to ${outputPath.pathname}`);

function htmlToText(html) {
  return html
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<br\s*\/?>/gi, "\n")
    .replace(/<\/p>/gi, "\n")
    .replace(/<img\b[^>]*alt="([^"]*)"[^>]*>/gi, "$1")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&quot;/g, "\"")
    .replace(/&ldquo;/g, "\"")
    .replace(/&rdquo;/g, "\"")
    .replace(/&lsquo;/g, "'")
    .replace(/&rsquo;/g, "'")
    .replace(/&#39;/g, "'")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&#(\d+);/g, (_, code) => String.fromCodePoint(Number(code)))
    .replace(/&#x([0-9a-f]+);/gi, (_, code) => String.fromCodePoint(Number.parseInt(code, 16)))
    .replace(/[ \t\r\f\v]+/g, " ")
    .replace(/\s*\n\s*/g, "\n")
    .trim();
}
